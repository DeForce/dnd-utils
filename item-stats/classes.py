class MagicItem:
    def __init__(self, image=None, type=None, sub_type=None, name=None, attunement=None, notes=None, rarity=None):
        self.image = image
        self.type = type
        self.subtype = sub_type
        self.name = name
        self.attunement = attunement
        self.notes = notes
        self.rarity = rarity
